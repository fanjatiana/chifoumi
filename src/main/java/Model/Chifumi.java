package Model;

public class Chifumi {
    private String chi;
    private String fu;
    private String mi;

    public Chifumi(String chi, String fu, String mi) {
        this.chi = chi;
        this.fu = fu;
        this.mi = mi;
    }

    public String getChi() {
        return chi;
    }

    public String getFu() {
        return fu;
    }

    public String getMi() {
        return mi;
    }
}
